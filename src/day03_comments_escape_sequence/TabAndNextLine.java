package day03_comments_escape_sequence;

public class TabAndNextLine {

    public static void main(String [] args){

        System.out.println("This is a sentence");
        System.out.println(" This is a sentence");
        System.out.println("  This is a sentence");
        System.out.println("   This is a sentence");
        System.out.println("    This is a sentence");
        System.out.println("\tThis is a sentence"); //1 tab equal 4 spaces
        System.out.println("\t\tThis is a sentence");

        System.out.println("1) Go to store \n2) Grav a bread\n3) Pay cashier");

    }

}
