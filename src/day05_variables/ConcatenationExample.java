package day05_variables;

public class ConcatenationExample {


    public static void main (String [] args) {



        System.out.println( 5+ 4);              // 9
        System.out.println(5 + 4 + "Hello");    // 9Hello
                        //int + int + String
                        // 9 (int) + String     // String

        System.out.println("Hello" + 5 + 4);    //Hello54
                        //String + int + int
                        //  String     + int  -- Hello5 + 4
                        //      String        -- Hello 54

    }


}
